export function getFixtureTarget(targetType: TargetType, targetId: string): GitLabTarget {
  return {
    id: targetId,
    ...fixtureTargets[targetType],
  };
}

export function getFixtureMergeRequests(targetType: TargetType): GitLabMergeRequest[] {
  return fixtureMergeRequests[targetType].map((mergeRequest) => ({
    ...mergeRequest,
    author: { ...mergeRequest.author },
    reviewers: mergeRequest.reviewers.map((reviewer) => ({ ...reviewer })),
  }));
}

function createPerson(id: number, name: string, username: string): GitLabPerson {
  return { id, name, username, webUrl: null };
}

function createMergeRequest({
  draft = false,
  webUrl,
  ...mergeRequest
}: Omit<GitLabMergeRequest, "draft" | "webUrl"> & {
  draft?: boolean;
  webUrl?: string;
}): GitLabMergeRequest {
  return {
    ...mergeRequest,
    draft,
    webUrl:
      webUrl ??
      `https://gitlab.example.com/${mergeRequest.projectPath}/-/merge_requests/${mergeRequest.iid}`,
  };
}